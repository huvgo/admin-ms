<template>
  <el-popover placement="bottom" title="通知" width="200" trigger="click">
    <div>XXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXX</div>
    <hr size="1" />
    <div>XXXXXXXXXXX</div>
    <hr size="5" />
    <div>XXXXXXXXXXX</div>
    <hr size="8" />
    <div slot="reference">
      <i class="el-icon-bell"></i>
    </div>
  </el-popover>
</template>

<script>
import screenfull from 'screenfull'

export default {
  name: 'Screenfull',
  data() {
    return {
      isFullscreen: false
    }
  },
  mounted() {
    this.init()
  },
  beforeDestroy() {
    this.destroy()
  },
  methods: {
    click() {
      if (!screenfull.enabled) {
        this.$message({
          message: 'you browser can not work',
          type: 'warning'
        })
        return false
      }
      screenfull.toggle()
    },
    change() {
      this.isFullscreen = screenfull.isFullscreen
    },
    init() {
      if (screenfull.enabled) {
        screenfull.on('change', this.change)
      }
    },
    destroy() {
      if (screenfull.enabled) {
        screenfull.off('change', this.change)
      }
    }
  }
}
</script>

